# คำนวณภาษี
def คำนวนภาษี(price):
   เรทภาษี = 7  # อัตราภาษี 7%
   ภาษี = (price * เรทภาษี) / 100
   return ภาษี

# รับข้อมูลสินค้าจากผู้ใช้
def รับข้อมูลสินค้า():
   ชื่อสินค้า = input("กรุณาใส่ชื่อสินค้า: ")
   ราคาสินค้า = float(input("กรุณาใส่ราคาสินค้า: "))
   return ชื่อสินค้า, ราคาสินค้า

# ฟังก์ชันสำหรับแสดงผลลัพธ์
def แสดงผลลัพธ์(ชื่อสินค้า, ราคาสินค้า, ภาษี):
   print(f"ชื่อสินค้า: {ชื่อสินค้า}")
   print(f"ราคาสินค้า: {ราคาสินค้า} บาท")
   print(f"ภาษีที่คิดได้: {ภาษี:.2f} บาท")

# เรียกใช้งานฟังก์ชัน
ชื่อสินค้า, ราคาสินค้า = รับข้อมูลสินค้า()
ภาษี = คำนวนภาษี(ราคาสินค้า)
แสดงผลลัพธ์(ชื่อสินค้า, ราคาสินค้า, ภาษี)