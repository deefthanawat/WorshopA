# คำนวณค่า y จากสมการ
def คำนวนy(x):
   return 2 * x**2 + 2 * x + 1

# รับค่า x จาก input
def รับค่าx():
   x = float(input("กรุณาใส่ค่า x: "))
   return x

# แสดงผลลัพธ์ y 
def แสดงผลลัพธ์y(x, y):
   print(f"ค่า x = {x} ให้ค่า y = {y}")

# เรียกใช้งานฟังก์ชัน
x = รับค่าx()
ผลลัพธ์ = คำนวนy(x)
แสดงผลลัพธ์y(x, ผลลัพธ์)